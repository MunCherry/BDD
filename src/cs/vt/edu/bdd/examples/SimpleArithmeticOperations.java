public class SimpleArithmeticOperations {

    //
    public int add(int no1, int no2) {
        return no1 + no2;
    }

    public int substract(int no1, int no2) {
        return no1 - no2;
    }

    public int divide(int no1, int no2) {
        return no1 / no2;
    }

    public int multiply(int no1, int no2) {
        return no1 * no2;
    }
}
